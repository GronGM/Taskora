<?php

namespace App\Http\Controllers\Public;

use App\Http\Controllers\Controller;
use App\Models\Order;
use Inertia\Inertia;
use Inertia\Response;

class HelpController extends Controller
{
    public function howItWorks(): Response
    {
        return Inertia::render('Help/HowItWorks', [
            'feePercent' => (float) config('payments.platform_fee_percent', 15),
            'reviewHold' => [
                'min' => Order::REVIEW_HOLD_MIN_DAYS,
                'max' => Order::REVIEW_HOLD_MAX_DAYS,
                'default' => Order::REVIEW_HOLD_DEFAULT_DAYS,
            ],
        ]);
    }

    public function faq(): Response
    {
        return Inertia::render('Help/Faq', [
            'items' => $this->faqItems(),
        ]);
    }

    /**
     * @return array<int, array{question: string, answer: string}>
     */
    private function faqItems(): array
    {
        $fee = (float) config('payments.platform_fee_percent', 15);
        $feeLabel = rtrim(rtrim(number_format($fee, 2, ',', ' '), '0'), ',');

        return [
            [
                'question' => 'Сколько стоит пользоваться Таскорой?',
                'answer' => "Размещение заданий и регистрация бесплатны. Комиссия платформы — {$feeLabel}% от суммы заказа, она видна до подтверждения сделки. Никаких скрытых платежей и платного продвижения нет.",
            ],
            [
                'question' => 'Как защищена оплата?',
                'answer' => 'Оплата за заказ удерживается платформой до приемки: исполнитель получает деньги только после того, как заказчик подтвердил результат. Пока идет проверка, средства никуда не уходят.',
            ],
            [
                'question' => 'Когда исполнитель получает деньги?',
                'answer' => 'Сразу после того, как заказчик принял работу. Если заказчик не проверил работу за выбранный срок проверки, оплата разблокируется автоматически.',
            ],
            [
                'question' => 'Что такое срок проверки работы?',
                'answer' => 'Это время после сдачи работы, в течение которого деньги остаются замороженными, а заказчик может проверить результат, запросить доработку или открыть спор. Срок выбирает заказчик при создании задания или заказа услуги; исполнители видят его заранее.',
            ],
            [
                'question' => 'Что делать, если работа не устроила?',
                'answer' => 'Пока оплата заморожена, можно запросить доработку или открыть спор — его рассмотрит модератор и решит: вернуть деньги, выплатить исполнителю или отправить работу на доработку. Все сообщения и файлы сделки хранятся внутри платформы и учитываются при решении.',
            ],
            [
                'question' => 'Можно ли обмениваться контактами и работать напрямую?',
                'answer' => 'Нет. Контакты и предложения увести сделку с платформы блокируются автоматически. Это правило защищает обе стороны: без сделки внутри Таскоры не работают удержание оплаты, споры и отзывы.',
            ],
            [
                'question' => 'Как выбираются исполнители и что значат уровни?',
                'answer' => 'Уровни (Новичок, Специалист, Профи, Эксперт) считаются только по объективным метрикам: завершенным оплаченным заказам, среднему рейтингу и доле проигранных споров. Купить уровень или продвижение нельзя.',
            ],
            [
                'question' => 'Настоящие ли отзывы на платформе?',
                'answer' => 'Отзыв можно оставить только по своему завершенному и оплаченному заказу — один отзыв на заказ. Рейтинги пересчитываются автоматически по опубликованным отзывам, искусственных оценок нет.',
            ],
            [
                'question' => 'Кто может стать исполнителем?',
                'answer' => 'Любой зарегистрированный пользователь с ролью исполнителя. Публикация услуг проходит модерацию, профиль можно отправить на ручную проверку и получить отметку «Проверен».',
            ],
            [
                'question' => 'Как пригласить друзей?',
                'answer' => 'У каждого пользователя есть персональная реферальная ссылка на странице «Приглашайте друзей». Приглашенные закрепляются за вами; бонусная программа заработает вместе с подключением реальных платежей.',
            ],
        ];
    }
}
